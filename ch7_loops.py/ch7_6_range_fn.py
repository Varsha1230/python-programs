# it will start from 0------------------------------
for i in range(8):
    print(i)
print("started from 0")

#if we want to start from 1--------------------------
for i in range(1,8):
    print(i)
print("started from 1")

#using step-size--------------------------------------
for i in range(0,8,2):
    print(i)
print("step size by 2, i.e. no. will be printed after 1-1 gap")
